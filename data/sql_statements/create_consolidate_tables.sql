CREATE TABLE IF NOT EXISTS CONSOLIDATE_STATION  (
    ID VARCHAR NOT NULL,
    CODE VARCHAR NOT NULL,
    NAME VARCHAR,
    CITY_NAME VARCHAR,
    CITY_CODE VARCHAR,
    ADDRESS VARCHAR,
    LONGITUDE FLOAT,
    LATITUDE FLOAT,
    STATUS VARCHAR,
    CREATED_DATE DATE,
    CAPACITTY INTEGER,
    PRIMARY KEY (ID, CREATED_DATE)
);

CREATE TABLE IF NOT EXISTS CONSOLIDATE_CITY (
    ID VARCHAR,
    NAME VARCHAR,
    NB_INHABITANTS INTEGER,
    CREATED_DATE VARCHAR,
    PRIMARY KEY (ID, CREATED_DATE)
);

CREATE TABLE IF NOT EXISTS CONSOLIDATE_STATION_STATEMENT (
    STATION_ID VARCHAR NOT NULL,
    BICYCLE_DOCKS_AVAILABLE INTEGER,
    BICYCLE_AVAILABLE INTEGER,
    LAST_STATEMENT_DATE DATE,
    CREATED_DATE VARCHAR,
    PRIMARY KEY (STATION_ID, CREATED_DATE)
);

CREATE TABLE IF NOT EXISTS CONSOLIDATE_COMMUNES(
    id VARCHAR NOT NULL,
    name VARCHAR ,
    department_code VARCHAR,
    region_code VARCHAR,
    postal_codes VARCHAR,
    population INTEGER,
    CREATED_DATE VARCHAR,
    PRIMARY KEY (id, CREATED_DATE)
);